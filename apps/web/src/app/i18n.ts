export function getActionLabel(locale: Locale, action: 'buy' | 'sell' | 'wait' | 'partial', mode: 'buy' | 'sell') {
  if (action === 'partial') {
    return locale === 'es'
      ? mode === 'buy' ? copy.es.partialBuy : copy.es.partialSell
      : mode === 'buy' ? copy.en.partialBuy : copy.en.partialSell;
  }

  return copy[locale][action];
}
